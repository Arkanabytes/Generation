package C20170625;

public class DoWhile {
    public static void main(String[] args) {
        int suma = 0;

        // Estructura simple de un For
        for (int i = 1; i <= 5; i++) {
            System.out.println("Numero: " + i);
            suma += i;

            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            /*
            1
            2
            3
            4
            5
            6
             */
        }
    }
}
