package C20160625;

public class Switch3 {
    public static void main(String[] args) {
        char letra = 'a';
        switch (letra) {
            case 'a':
                System.out.println("Letra A");
                break;
            case 'b':
                System.out.println("Letra B");
                break;
            case 'c':
                System.out.println("Letra C");
                break;
            default:
                System.out.println("No es una letra valida");
        }
    }

}

